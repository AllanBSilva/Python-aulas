#5) Você está em uma sala com três interruptores, cada um conectado a uma lâmpada em salas diferentes. Você não pode ver as lâmpadas da sala em que está, mas pode ligar e desligar os interruptores quantas vezes quiser. Seu objetivo é descobrir qual interruptor controla qual lâmpada. Como você faria para descobrir, usando apenas duas idas até uma das salas das lâmpadas, qual interruptor controla cada lâmpada?  

# Resposta:

# 1. Ligue o primeiro interruptor e deixe-o ligado por cerca de 5-10 minutos.
#    Isso permitirá que a lâmpada esquente.

# 2. Desligue o primeiro interruptor e ligue o segundo interruptor.

# 3. Entre na sala que a luz ligada.

# 4. Entre na segunda sala e veja se a lâmpada está quente, com isso saberá se corresponde ao interruptor 1.

# Identificação dos interruptores:
# - A lâmpada que está acesa corresponde ao segundo interruptor (aquele que você deixou ligado).
# - A lâmpada que está apagada, mas quente, corresponde ao primeiro interruptor (aquele que você deixou ligado antes e depois desligou).
# - A lâmpada que está apagada e fria corresponde ao terceiro interruptor (aquele que nunca foi ligado).

# Dessa forma, você consegue identificar qual interruptor controla cada lâmpada usando apenas duas idas à sala das lâmpadas!
